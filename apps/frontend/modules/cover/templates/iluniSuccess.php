<h4>AlumniBook for ILUNI</h4>

<p>This page is dedicated to our beloved ILUNI,
the foundation that holds alumni community, University of Indonesia.</p>

<p>This is a rewrite version of the old AlumniBook software created years ago.
Although I've made it for learning purpose, it is a really <b>functional</b> data entry software.
I maintain similar feature as the old one, the strength and also its limitation.
You can utilize it as well as the old one.</p>


<h4>Who is this project for?</h4>

<p>This software is just a tool to create printed book of alumni.
We use the old one to create <i>alumni business directory</i>
Since the book containing two section,
it is the alumni section and the business section,
the database schema needs to map relationship between them.
The business process of the printed book making,
also need manual verification to avoid any human errors and sometimes fake data.
Thus, it is intended to be a data entry software in a small network,
not for the internet which each alumna/us can submit their data itself.
</p>

<p>This site and any preloaded data is only a demonstration of its capability.
It is not intended for real data entry process.</p>


<h4>What if I want to use it?</h4>

<p>If you need to use it,
you just need to find someone, a student or fresh graduate,
to install and maintain alumni's database.
