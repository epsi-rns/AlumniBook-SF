<h4>AlumniBook on Symfony</h4>

<p>This is my first symfony application.
For learning purpose so others may have sample
and speed up their development process.</p>

<p>This is just another unofficial sample of symfony application.
Several common tricks for data entry problem has been solved,
just take a look at the source code.</p>

<p>More about this project: &#x00bb; &nbsp;
<a class="readon" href="https://github.com/epsi/AlumniBook-SF">AlumniBook on Github</a>
