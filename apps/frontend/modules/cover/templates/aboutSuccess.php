<h4>Featuring</h4>

<ul>
	<li>
	Included many good things from Jobeet documentation from
	<a href="http://www.symfony-project.org/jobeet/1_4/Doctrine/en/"></a>
	I also skipped many other good parts.
	</li>
	<li>Simple routing class for master-detail table relationship.</li>
	<li>
	Base component to render table sheet report
	with header, footer and grouping feature.</li>
	<li>Different layouts: layout, error404, ajax, and modal.</li>
	<li>
	<b>Mootools integration:</b>
	<ul>
		<li>ajax request</li>
		<li>form field validation</li>
		<li>theme effects</li>
		<li>sfWidgetFormDatePicker</li>
		<li>dynamic drop down list using ajax (master-detail)</li>
		<li>Using a special iframe lightbox for value lookup.
		This input also utilize a lookup modal widget.</li>
	</ul>
	</li>
	<li>
	<b>Oriclone Theme:</b>
	My personal theme, the original clone.
	Although I've made it from scratch, it is inspired by other design.
	I also have included some source images.
	</li>
	<li>Few plugins: 
	each has their own README.md, 
	most are only mootools wrapper.</li>
	<li>Also utilize I18n translation in form filter.</li>
</ul>
